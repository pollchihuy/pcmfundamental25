package com.juaracoding.berbagailogika;

public class FungsiMath {

    public static void main(String[] args) {
        System.out.println(Math.pow(2.0,3.0));
        System.out.println(Math.sqrt(5.0));

        System.out.println(Math.round(4.5));
        System.out.println("Ceil : "+Math.ceil(4.000000000000001));
        System.out.println("000000000000001".length());
        System.out.println("Floor : "+Math.floor(4.9999999999999999));
    }
}
